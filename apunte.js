/*
** ¿Qué es un objeto en JavaScript? **

Un objeto es una colección de propiedades, y una propiedad es una asociación entre un nombre (o clave) y un valor. Un objeto puede contener propiedades que son de cualquier tipo, incluyendo otros objetos y funciones (a estas últimas se les llama métodos).
*/


/*
** ¿Qué es una clase en JavaScript? **

Las clases en JavaScript son una forma de crear objetos y establecer su estructura y comportamiento. Las clases proporcionan una sintaxis más clara y simplificada para crear y manejar objetos, especialmente cuando se trabaja con la herencia y la reutilización de código. Aquí tienes una explicación de la relación entre clases y objetos:

¿Qué es una clase?
Una clase es un plano o molde que define las propiedades y métodos que tendrán los objetos creados a partir de ella.
*/


